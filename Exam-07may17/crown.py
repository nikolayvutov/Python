n = int(input())

print('@' + ' ' * (n-2) + '@' + ' ' * (n-2) + '@')

print('**' + ' ' * (n-3) + '*' + ' ' * (n-3) + '**')

for i in range(1, (n//2)):
    print('*'+ '.' * i + '*' + ' ' * ((n//2)-i) + '*'+ '.' * (i) + '*' + ' ' * ((n//2)-i) + '*'+ '.' * i + '*')
    i -= 2

print('*' + '.' * (n//2) + '*' *(n-(n//2)-2) + '.' + '*' *(n-(n//2)-2) + '.' * (n//2) + '*')
print('*' * (2*n-1))
print('*' * (2*n-1))