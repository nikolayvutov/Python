n = int(input())

print('+' + '~' * (n-2) + '+' + '.' * (2*n+1))
print('|' + '\\' + '~' * (n-2) + '\\' + '.' * (2*n))

for i in range(1, 2*n+1):
    print('|' + '.' * i + '\\' + '~' * (n-2) + '\\' + '.' * (2*n - i))
for i in range(2*n+1):
    print('.'* i + '\\' + '.' * (2*n - i) + '|' + '~' * (n-2) + '|' )
print('.' * (2*n+1) + '+' + '~' * (n-2) + '+')